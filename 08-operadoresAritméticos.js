// Adição (+)
let soma = 10 + 5; // soma = 15
console.log(`soma: ${soma}`)

// Subtração (-)
let subtracao = 20 - 6; // subtracao = 14
console.log(`subtração: ${subtracao}`)

// Multiplicação (*)
let multiplicacao = 7 * 3; // multiplicacao = 21
console.log(`Multiplicação: ${multiplicacao}`)

// Divisão (/)
let divisao = 15 / 4; // divisao = 3.75
console.log(`Divisão: ${divisao}`)

// esto da divisão (%) (Operador Módulo)
let resto = 15 % 4; // resto = 3 (pois 15 ÷ 4 é 3 com resto 3)
console.log(`Módulo: ${resto}`)

// Incremento (++)
let numeroIncremento = 5;
numeroIncremento++; // agora numero = 6
console.log(`incremento: ${numeroIncremento}`)

// Decremento (--)
let numeroDecremento = 8;
numeroDecremento--; // agora numero = 7
console.log(`Decremento: ${numeroDecremento}`)

// Operadores de atribuição combinados
let valor = 10;
valor += 5; // equivalente a valor = valor + 5 (resultado: 15)
console.log(valor)

let outroValor = 20;
outroValor -= 3; // equivalente a outroValor = outroValor - 3 (resultado: 17)
console.log(outroValor)

//Operador *= (Multiplicação e atribuição)
let numero1 = 10;
numero1 *= 3; // equivalente a numero = numero * 3 (resultado: 30)
console.log(numero1); // Saída: 30

// Operador /= (Divisão e atribuição)
let valor2 = 50;
valor2 /= 5; // equivalente a valor = valor / 5 (resultado: 10)
console.log(valor2); // Saída: 10

// Operador %= (Módulo e atribuição)
let numero2 = 21;
numero2 %= 4; // equivalente a numero = numero % 4 (resultado: 1)
console.log(numero2); // Saída: 1
